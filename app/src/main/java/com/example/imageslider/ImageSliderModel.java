package com.example.imageslider;


//This is Model Class
public class ImageSliderModel {

    //Import a int Method and Constructor it and do Getter and Setter
    int image;

    public ImageSliderModel() {
    }

    public ImageSliderModel(int image) {
        this.image = image;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
